document.getElementById("b717").addEventListener("click", function(){
    const div = document.getElementById("div_717");
    const BoeingPlane = document.getElementById("boeing_717");
    if (div.style.display === "none" || div.style.display === "") {
        div.style.display = "block";
        const currentheight = parseInt(window.getComputedStyle(BoeingPlane).height,10);
        BoeingPlane.style.height = (currentheight + 50) + "px";
    } else {
        div.style.display = "none";
        const currentheight = parseInt(window.getComputedStyle(BoeingPlane).height,10);
        BoeingPlane.style.height = (currentheight - 50) + "px";
    }
    

});


